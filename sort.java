package com.pro.java;

public class sort {
    public static void main(String[] args) {
        int arr[] = {1, 43, 54, 23, 5, 0};
        bubbleSort(arr);
        printArray(arr);
    }

    public static void bubbleSort(int arr[]) {
        int n = arr.length;
        boolean swapped;
        do {
            swapped = false;
            for (int i = 1; i < n; i++) {
                if (arr[i - 1] > arr[i]) {
                   
                    int temp = arr[i - 1];
                    arr[i - 1] = arr[i];
                    arr[i] = temp;
                    swapped = true;
                }
            }
        } while (swapped);
    }

    public static void printArray(int arr[]) {
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
        System.out.println();
    }
}
